    /// <summary>
    /// 参考自 liangxie QFramework https://github.com/liangxiegame/QFramework
    /// </summary>
    public interface IAkiEvent
    {
    }
    
    public class AkiEvent : IAkiEvent
    {
        private Action mOnEvent = () => { };

        public IUnRegister Register(Action onEvent)
        {
            mOnEvent += onEvent;
            return new CustomUnRegister(() => { UnRegister(onEvent); });
        }

        public void UnRegister(Action onEvent)
        {
            mOnEvent -= onEvent;
        }

        public void Trigger()
        {
            mOnEvent?.Invoke();
        }
    }

